
public class Else_If_Statement
{

	public static void main(String[] args) 
	{
		
		int a = 10;
		if (a > 20)
		System.out.println(+a+  " a is greater than 10"); // if a is true then only print 
		else if (a == 20)
		System.out.println(+a+ " a is equal to 20"); // else if is true then only print
		else
		System.out.println(+a+ " a is less than 20"); // if & else if a is false then only print
	}

}
